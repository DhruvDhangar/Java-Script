const val = 300

switch (val) {
    case 100:
        console.log("Value is 100");
        break;
    case 200:
        console.log("Value is 200");
        break;
    case 300:
        console.log("Value is 300");
        break;
    case 400: 
        console.log("Value is 400");
        break;
    default:
        console.log("Value is 500");
        break;
}